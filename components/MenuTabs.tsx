"use client";

import { useMemo, useState } from "react";

type MenuItem = {
  name: string;
  meta?: string;
  price: string;
  note?: string;
};

type MenuSection = {
  title: string;
  hint?: string;
  items: MenuItem[];
};

function Pill({ children, active, onClick }: { children: React.ReactNode; active?: boolean; onClick?: () => void }) {
  return (
    <button
      type="button"
      onClick={onClick}
      className={
        "btn-lux inline-flex items-center justify-center rounded-full px-4 py-2 text-sm font-semibold border transition " +
        (active
          ? "bg-[color:var(--dark-blue)] text-white border-[color:var(--dark-blue)]"
          : "bg-white/70 text-[color:var(--dark-blue)] border-[color:var(--light-gray)] hover:bg-white")
      }
      aria-pressed={active}
    >
      {children}
    </button>
  );
}

function SectionBlock({ s }: { s: MenuSection }) {
  return (
    <div className="card-lux rounded-3xl border border-[color:var(--light-gray)] bg-white shadow-sm overflow-hidden">
      <div className="p-6">
        <div className="flex items-baseline justify-between gap-4">
          <h3 className="heading-font text-xl font-extrabold text-[color:var(--dark-blue)]">{s.title}</h3>
          {s.hint ? <p className="text-xs text-slate-500">{s.hint}</p> : null}
        </div>
        <div className="mt-4 space-y-3">
          {s.items.map((i) => (
            <div key={i.name + i.price} className="flex items-start justify-between gap-4">
              <div className="min-w-0">
                <p className="text-sm md:text-[15px] font-semibold text-slate-900 leading-snug">{i.name}</p>
                {i.meta ? <p className="text-xs text-slate-500 mt-0.5">{i.meta}</p> : null}
                {i.note ? <p className="text-xs text-slate-600 mt-1">{i.note}</p> : null}
              </div>
              <div className="shrink-0 text-sm font-extrabold text-[color:var(--dark-blue)] tabular-nums">{i.price}</div>
            </div>
          ))}
        </div>
      </div>
    </div>
  );
}

